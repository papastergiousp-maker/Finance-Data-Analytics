import plotly.graph_objects as go # Εισάγετε την απαιτούμενη βιβλιοθήκη

# Ορίστε τα δεδομένα για το διάγραμμα
items = ["Item1", "Item2", "Item3"]
x_data = [10, 15, 20]
y_data = [20, 25, 30]
z_data = [5, 10, 15]

# Δημιουργήστε ένα τρισδιάστατο διάγραμμα διασποράς χρησιμοποιώντας τα δεδομένα
fig = go.Figure(
    go.Scatter3d(
        x=x_data, # Ορίστε τα δεδομένα του άξονα x
        y=y_data, # Ορίστε τα δεδομένα του άξονα y
        z=z_data, # Ορίστε τα δεδομένα του άξονα z
        mode="markers", # Ορίστε τη λειτουργία του διαγράμματος για την εμφάνιση δεικτών
        marker=dict(size=5, color="purple"), # Ορίστε το μέγεθος και το χρώμα του δείκτη
        text=items, # Ορίστε τις ετικέτες κειμένου για κάθε δείκτη
    )
)

# Ενημερώστε τη διάταξη του διαγράμματος
fig.update_layout(
    scene=dict(
        xaxis_title="X Axis Label", # Ορίστε τον τίτλο του άξονα x
        yaxis_title="Y Axis Label", # Ορίστε τον τίτλο του άξονα y
        zaxis_title="Z Axis Label", # Ορίστε τον τίτλο του άξονα z
        bgcolor="lightgrey", # Ορίστε το χρώμα φόντου της τρισδιάστατης σκηνής
    ),
    plot_bgcolor="white", # Ορίστε το χρώμα φόντου του διαγράμματος
    title="3D Scatter Plot", # Ορίστε τον τίτλο του διαγράμματος
)

fig.show() # Εμφανίστε το διάγραμμα